package com.kgisl.qs1;

class emp {
    int empid;    
    String name;    
    int Salary;

     emp(int empid,String name,int Salary){    
     this.empid=empid;    
     this.name=name;    
     this.Salary=Salary;    
     }  
   
     public int getid() {  
         return empid;  
     }  
   
     public void setid(int empid) {  
         this.empid = empid;  
     }  
   
     public String getName() {  
         return name;  
     }  
   
     public void setName(String name) {  
         this.name = name;  
     }  
   
     public int getSalary() {  
         return Salary ;  
     }  
   
     public void setSalary(int Salary) {  
         this.Salary = Salary;  
     }  
   
     }    